#include "ft_string.h"

char			*ft_revstr(char *str)
{
	int		i;
	int		len;

	i = 0;
	len = ft_strlen(str);
	while (i < (len / 2))
	{
		ft_swap_char(&str[i], &str[len - i - 1]);
		i++;
	}
	return (str);
}
