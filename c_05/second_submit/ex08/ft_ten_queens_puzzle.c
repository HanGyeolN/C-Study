void	set_queen(int row, int col, int mod)
{
	int r;
	int c;

	r = -1;
	c = -1;
	while (++r < 10 && ++c < 10)
	{
		g_chessboard[r][col] += mod;
		g_chessboard[row][c] += mod;
		if (row + r < 10 && col + c < 10)
			g_chessboard[row + r][col + c] += mod;
		if (row + r < 10 && col - c >= 0)
			g_chessboard[row + r][col - c] += mod;
		if (row - r >= 0 && col + c < 10)
			g_chessboard[row - r][col + c] += mod;
		if (row - r >= 0 && col - c >= 0)
			g_chessboard[row - r][col - c] += mod;
	}
	if (mod == 1)
		g_chessboard[row][col] = 'Q';
	else if (mod == -1)
		g_chessboard[row][col] = '0';
}

