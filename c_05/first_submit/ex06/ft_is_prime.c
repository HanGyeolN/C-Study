int	ft_is_prime(int nb)
{
	unsigned int	i;

	if (nb < 2)
		return (0);
	else if (nb == 2 || nb == 3 || nb == 5)
		return (1);
	else if (nb % 2 == 0)
		return (0);
	i = 3;
	while (i * i <= nb)
	{
		if (nb % i == 0)
			return (0);
		i = i + 2;
	}
	return (1);
}
