int	ft_find_next_prime(int nb)
{
	int i;
	int mod;

	i = 1;
	if (nb < 3)
		return (2);
	while (1)
	{
		i++;
		mod = nb % i;
		if (mod == 0)
		{
			i = 1;
			nb++;
		}
		else if (i >= nb / 2)
		{
			return (nb);
		}
	}
}
