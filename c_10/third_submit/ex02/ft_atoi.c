int		ft_atoi(char *str)
{
	int		res;
	int		sign;

	res = 0;
	sign = 1;
	if (*str == '-')
	{
		sign = -1 * sign;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		res = res * 10 + (*str - '0') * sign;
		str++;
	}
	return (res);
}
